<script>
import AuthMixin from "./../../mixins/auth";
import { setTimeout } from "timers";
export default {
  name: "WhatsappPage",
  mixins: [AuthMixin],
  mounted() {
    var params = this.extractParams();

    this.getPreferences(
      params,
      function(success) {
        this.$parent.hotspot.disclaimers = success.body.disclaimers;
        this.$root.$options.hotspot.disclaimers = success.body.disclaimers;
        this.$root.$options.hotspot.integrations = success.body.integrations;
        this.hotspot.disclaimers = success.body.disclaimers;
        this.hotspot.preferences = success.body.preferences;
        this.textColor =
          success.body.preferences.captive_84_text_color || "#4A4A4A";
        this.textFont =
          success.body.preferences.captive_85_text_style || "Roboto";

        if (this.$route.query.integration_done) {
          var context = this;
          context.dedaloRequested = true;
          context.authorized = false;
          context.errors.dedaloError = false;
          setTimeout(function() {
            context.execLogin();
          }, 1000);
        }

        if (!this.$route.query.num && !this.$route.query.code) {
          this.getCode();
        } else if (this.$route.query.num && this.$route.query.code) {
          this.execLogin();
        }
      },
      function(error) {
        console.error(error);
      }
    );
  },
  data: function() {
    return {
      authorized: false,
      choosedMode:
        this.$route.query.num && this.$route.query.code ? true : false,
      codeRequested: this.$route.query.code || false,
      bannerShow: this.$route.query.code || false,
      openBtn: this.$route.query.temp || false,
      dedaloRequested: false,
      authNum: this.$route.query.num || "",
      authCode: this.$route.query.code || "",
      authReset: this.$route.query.code || false,
      userId: this.$route.query.user || 0,
      resetDone: false,
      errors: {
        badMail: false,
        badCode: false,
        dedaloError: false,
        badInput: false
      },
      hotspot: {
        disclaimers: this.$root.$options.hotspot.disclaimers,
        preferences: this.$root.$options.hotspot.preferences
      },
      conditions: false,
      surveys: false,
      countries: require("./../../i18n/countries.json"),
      additionalCountry: "-",
      additionalReason: "-",
      passwordVisible: true,
      textColor: "#4A4A4A",
      textFont: "Roboto"
    };
  },
  computed: {
    textStyle: function() {
      return {
        color: this.textColor,
        "font-family": this.textFont
      };
    },
    buttonStyle: function() {
      return {
        "font-family": this.textFont
      };
    }
  },
  methods: {
    back() {
      this.$router.push({
        path: "/login"
      });
    },
    isDisabled: function() {
      return this.authNum.length == 0 || this.authCode.length == 0;
    },
    chooseMode: function(haveCode) {
      this.choosedMode = true;
      if (haveCode) {
        this.codeRequested = true;
      }
    },
    redirectAuth() {
      window.location.replace(
        "http://wa.me/" +
          CONFIG.WHATSAPP_NUMBER +
          "?text=" +
          encodeURIComponent(this.$route.query.short_code)
      );
    },
    getCode: function(reset) {
      var params = this.extractParams();

      // open temp session for the user
      this.doTempSession(
        "",
        function(responseTmp) {
          var origin = "http://conncheck." + window.location.host;
          var pathname = window.location.pathname;
          var query =
            "?digest=" +
            params.digest +
            "&uuid=" +
            params.uuid +
            "&sessionid=" +
            params.sessionid +
            "&uamip=" +
            params.uamip +
            "&uamport=" +
            params.uamport +
            "&user=" +
            this.userId +
            "&short_code=" +
            responseTmp.body.short_code +
            "&temp=done";
          window.location.replace(origin + pathname + query);
        },
        function(error) {
          this.codeRequested = false;
          this.errors.badMail = true;
          console.error(error);
        }
      );
    },
    execLogin: function() {
      this.dedaloRequested = true;
      this.authorized = false;
      this.errors.dedaloError = false;
      this.errors.badCode = false;

      if (
        this.$root.$options.hotspot.integrations &&
        this.$root.$options.hotspot.integrations[0] &&
        this.$root.$options.hotspot.integrations[0].post_auth_redirect_url &&
        this.$root.$options.hotspot.integrations[0].post_auth_redirect_url
          .length > 0 &&
        !this.$route.query.integration_done
      ) {
        // go to post_auth_redirect_url
        var redirectUrl = this.$root.$options.hotspot.integrations[0]
          .post_auth_redirect_url;

        var params = this.extractParams();

        var query =
          "?digest=" +
          params.digest +
          "&uuid=" +
          params.uuid +
          "&sessionid=" +
          params.sessionid +
          "&uamip=" +
          params.uamip +
          "&uamport=" +
          params.uamport +
          "&user=" +
          this.userId +
          "&nasid=" +
          params.nasid +
          "&code=" +
          this.authCode +
          "&num=" +
          this.authNum;

        var pathname = window.location.pathname;

        this.doDedaloLogout(
          function(responseDedaloLogout) {
            window.location.replace(redirectUrl + pathname + query);
          },
          function(error) {
            this.authorized = false;
            this.errors.dedaloError = true;
            console.error(error);
          }
        );
      } else {
        // exec logout
        this.doDedaloLogout(
          function(responseDedaloLogout) {
            // exec dedalo login
            this.doDedaloLogin(
              {
                id: this.authNum,
                password: this.authCode || ""
              },
              function(responseDedalo) {
                if (responseDedalo.body.clientState == 1) {
                  this.authorized = true;
                  this.errors.dedaloError = false;
                } else {
                  this.authorized = false;
                  this.errors.dedaloError = true;
                  this.errors.badCode = true;
                }
              },
              function(error) {
                this.authorized = false;
                this.errors.dedaloError = true;
                console.error(error);
              }
            );
          },
          function(error) {
            this.authorized = false;
            this.errors.dedaloError = true;
            console.error(error);
          }
        );
      }
    },
    navigate() {
      if (this.conditions && this.surveys) {
        this.accept();
      } else {
        this.deleteInfo();
      }
    },
    deleteInfo: function() {
      var context = this;
      // extract code and state
      var params = this.extractParams();

      // delete marketing
      if (!this.conditions) {
        this.deleteMarketingInfo(
          this.userId,
          params,
          function(success) {
            context.accept();
          },
          function(error) {
            console.error(error);
            if (error.status == 404) {
              context.accept();
            }
          }
        );
      }

      if (!this.surveys) {
        this.deleteSurveyInfo(
          this.userId,
          params,
          function(success) {
            context.accept();
          },
          function(error) {
            console.error(error);
            if (error.status == 404) {
              context.accept();
            }
          }
        );
      }
    },
    accept: function() {
      var context = this;

      // extract code and state
      var params = this.extractParams();

      if (this.hotspot.preferences.marketing_0_reason_country == "true") {
        this.addAdditionalInfo(
          this.userId,
          params,
          {
            reason: this.additionalReason,
            country: this.additionalCountry
          },
          function(success) {
            // open redir url
            window.location.replace(
              context.$root.$options.hotspot.preferences.captive_1_redir
            );
          },
          function(error) {
            console.error(error);
            if (error.status == 404) {
              // open redir url
              window.location.replace(
                context.$root.$options.hotspot.preferences.captive_1_redir
              );
            }
          }
        );
      } else {
        // open redir url
        window.location.replace(
          context.$root.$options.hotspot.preferences.captive_1_redir
        );
      }
    },
    togglePasswordVisibility: function() {
      this.passwordVisible = !this.passwordVisible;
    }
  }
};
</script>

